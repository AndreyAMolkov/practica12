


/*Написать программу, которая находит в диапазоне целых чисел от
2 до 1000000 число, формирующее самую длинную последователь-
ность Коллатца
Пояснение

Последовательностью Коллатца называют числовой ряд, каждый элемент которого формируется в зависимости от чётности/ нечётности предыдущего позакону:

n→3n+1, если n нечётное.
n→n/2, если n чётное
Начав отсчет с любого числа у нас формируется последовательность Коллатца, например:

1
2,1
3,10,5,16,8,4,1
4,2,1
5,..
Генерация членов последовательности заканчивается, когда появляется единица. Количество элементов и составляет длину последовательности. В задаче надо перебирать в цикле числа от 2 до миллиона и для каждого считать длину последовательности. В конце необходимо вывести число, формирующего самую длинную последовательность и длину этой последовательности.

Состав

Программа должна состоять из функций:

    - unsigned int seqCollatz(unsigned int *maxlen) - функция, возвращающая число и записывающую по адресу len длину 
    - unsigned int collatz(unsigned long long num) - функция, возвращающая длину последовательности Коллатца для числа num
    - main() - организация ввода строки
Создаются три файла: task2.h,task2.c,main2.c.

*/